package fullstacktraining.springboot.react.model;

public class Message {

    private String msg;
    private Boolean err;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Boolean getErr() {
        return err;
    }

    public void setErr(Boolean err) {
        this.err = err;
    }


}
